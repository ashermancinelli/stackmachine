/*
 * All valid opcodes
 */
#[derive(Clone, PartialEq, Copy, Debug)]
#[allow(dead_code)]
#[repr(u16)]
pub enum Op {
    Const,
    Add,
    Sub,
    Mul,
    Div,
    Print,
    PrintStr,
    Pop,
    Push,
    PushStr,
    Noop,
    Block,
    Loop,
    Return,
    Break,
    CallExt,
    Call,
    Fork,
    If,
    Else,
    Not,
    GT,
    LT,
    GTE,
    LTE,
    r#Eq,
    EndIf,
    Function,
    EndFunction,
    GetPid,
    Child,
    Debug,
    Include,
}
